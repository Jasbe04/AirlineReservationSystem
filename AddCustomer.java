    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        
        
        try {
            
            String CustomerId=CusNum.getText();
            String FirstName=firstName.getText();
            String LastName=lastName.getText();
            String Passport=PassNumber.getText();
            String NationalID=NIDNumber.getText();
            String Address=addressTextArea.getText();
            String ContractNum=contractNumber.getText();
            String Gender="";
            if(male.isSelected())
            {
                Gender="Male";
            }
            else 
                Gender="Female";
            DateFormat da=new SimpleDateFormat("YYYY-MM-DD");
            String date=da.format(this.date.getDate());
            
            Connection con;
            PreparedStatement pre;
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/airline_project", "root", "your_password_here");
            
            
            pre =con.prepareStatement("insert into customer(CustomerID,FirstName,LastName,Passport,NationalID,Address,Contract,Gender,DOB) values(?,?,?,?,?,?,?,?,?);");
            pre.setString(1, CustomerId);
            pre.setString(2, FirstName);
            pre.setString(3, LastName);
            pre.setString(4, Passport);
            pre.setString(5, NationalID);
            pre.setString(6, Address);
            pre.setString(7, ContractNum);
            pre.setString(8, Gender);
            pre.setString(9, date);
            
            pre.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Customer Added Successfully!");
        
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null,"Class Not Found!","Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
         catch(SQLException e)
         {
            JOptionPane.showMessageDialog(null,"SQL Exception ","Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
         }
    }//GEN-LAST:event_AddBtnActionPerformed

