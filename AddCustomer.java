   public void AutoId() {
    try {
        // Load driver
        Class.forName("com.mysql.cj.jdbc.Driver");

        // Connect to database
        Connection conn = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/airline_project", "root", "your_password_here");

        // Query to get the max ID
        PreparedStatement pre = conn.prepareStatement("SELECT MAX(CustomerID) AS MaxID FROM Customer;");
        ResultSet rs = pre.executeQuery();

        String newId;

        if (rs.next()) {
            String maxId = rs.getString("MaxID");

            if (maxId == null) {
                newId = "CS01";
            } else {
                long id = Long.parseLong(maxId.substring(2)); // Skip "CS"
                id++;
                newId = "CS" + String.format("%02d", id);
            }

            CusNum.setText(newId);
        }

        // Close resources
        rs.close();
        pre.close();
        conn.close();

    } catch (ClassNotFoundException ex) {
        System.out.println(ex.getMessage());
    } catch (SQLException ex) {
System.out.println(ex.getMessage());    }
}

    

